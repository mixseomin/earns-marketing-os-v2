import type { Mode, Squad, Kpi, Column, Card, FeedEvent, Alert } from '../types';

function sq(id: string, name: string, vi: string, icon: string, agents: number, active: number, color: string, desc: string, health: 'ok' | 'warn' | 'bad' = 'ok'): Squad {
  return { id, name, vi, icon, agents, active, color, desc, health };
}
function kpi(label: string, unit: string | undefined, val: string | number, suffix: string | undefined, delta: string | undefined, tone: string | undefined, spark?: number[], color?: string, primary?: boolean): Kpi {
  return { label, unit, val, suffix, delta, tone: tone as Kpi['tone'], spark: spark || [40, 45, 48, 44, 50, 52, 48, 55, 58, 55, 60, 62, 60, 65], color: color || 'var(--accent)', primary: !!primary };
}
function col(id: string, title: string, vi: string, icon: string, tone: string, limit?: number): Column {
  return { id, title, vi, icon, tone, limit };
}
function card(id: string, col: string, title: string, squad: string, level: 1 | 2 | 3 | 4, money: string | null | undefined, due: string, tags: string[], agent: string, body?: string, urgent?: boolean): Card {
  return { id, col, title, squad, level, money, due, tags, agent, body, urgent: !!urgent };
}
function fi(t: string, agent: string, lvl: 1 | 2 | 3 | 4, action: string, target: string, isnew?: boolean): FeedEvent {
  return { t, agent, lvl, action, target, new: !!isnew };
}
function al(id: string, tone: 'bad' | 'warn' | 'info' | 'ok', title: string, body: string, time: string, tags: string[]): Alert {
  return { id, tone, title, body, time, tags };
}

