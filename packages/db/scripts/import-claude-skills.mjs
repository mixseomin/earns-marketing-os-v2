#!/usr/bin/env node
// Reads all SKILL.md files under ~/.claude/skills/, parses frontmatter,
// generates a Drizzle migration that INSERTs them into skill_snippets.
//
// Usage: node packages/db/scripts/import-claude-skills.mjs > packages/db/migrations/0013_claude_skills.sql

import fs from 'node:fs';
import path from 'node:path';
import os from 'node:os';

const ROOT = path.join(os.homedir(), '.claude', 'skills');

function* walk(dir) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, entry.name);
    if (entry.isDirectory()) yield* walk(p);
    else if (entry.isFile() && entry.name === 'SKILL.md') yield p;
  }
}

// Tiny YAML frontmatter parser — supports `key: value` and `key: > \n  multiline`.
function parseFrontmatter(text) {
  const m = text.match(/^---\n([\s\S]*?)\n---\n?([\s\S]*)$/);
  if (!m) return { meta: {}, body: text };
  const [, fm, body] = m;
  const meta = {};
  const lines = fm.split('\n');
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const km = line.match(/^([a-zA-Z_][a-zA-Z0-9_]*):\s*(.*)$/);
    if (!km) continue;
    const [, key, rawVal] = km;
    if (rawVal === '>' || rawVal === '|' || rawVal === '') {
      // Multi-line block: collect indented continuation lines.
      const acc = [];
      while (i + 1 < lines.length && /^\s+/.test(lines[i + 1])) {
        acc.push(lines[++i].replace(/^\s+/, ''));
      }
      meta[key] = acc.join(rawVal === '>' ? ' ' : '\n').trim();
    } else {
      meta[key] = rawVal.trim().replace(/^["']|["']$/g, '');
    }
  }
  return { meta, body: body.trimStart() };
}

// PostgreSQL escape: use E'...' with \\ and \' escapes.
function pgEscape(s) {
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'").replace(/\r/g, '');
}

const rows = [];
for (const file of walk(ROOT)) {
  const raw = fs.readFileSync(file, 'utf8');
  const { meta, body } = parseFrontmatter(raw);
  const dirName = path.basename(path.dirname(file));      // 'affiliate-strategist'
  const category = path.basename(path.dirname(path.dirname(file))); // 'marketing'
  // Edge case: top-level skills (e.g. /skills/facebook/SKILL.md) → category = 'skills'
  const cat = category === 'skills' ? dirName : category;

  // Slug: include category prefix to avoid collisions (engineering vs marketing both have 'content-creator'?)
  // Use just dirName as slug if unique; will rely on ON CONFLICT DO NOTHING.
  const slug = `cc-${dirName}`;
  const title = meta.name || dirName.replace(/-/g, ' ').replace(/\b\w/g, (c) => c.toUpperCase());
  const description = meta.description || '';
  // Combine description (frontmatter) with body for the snippet.
  const fullBody = description
    ? `> ${description.replace(/\n/g, '\n> ')}\n\n${body}`
    : body;
  const tags = [cat, 'claude-code'];

  rows.push({ slug, title, body: fullBody, tags });
}

// Sort for deterministic output
rows.sort((a, b) => a.slug.localeCompare(b.slug));

// Emit SQL
process.stdout.write(`-- Auto-generated by scripts/import-claude-skills.mjs\n`);
process.stdout.write(`-- Imported ${rows.length} SKILL.md files from ~/.claude/skills/\n`);
process.stdout.write(`-- Source: local Claude Code skills collection.\n\n`);
process.stdout.write(`INSERT INTO skill_snippets (tenant_id, slug, title, body, tags, source, source_url, license) VALUES\n`);
const values = rows.map((r) => {
  const tagsJson = JSON.stringify(r.tags);
  return `  ('self', '${r.slug}', E'${pgEscape(r.title)}', E'${pgEscape(r.body)}', '${tagsJson}'::jsonb, 'claude-code-skills', NULL, 'curated')`;
});
process.stdout.write(values.join(',\n'));
process.stdout.write(`\nON CONFLICT (tenant_id, slug) DO NOTHING;\n`);

process.stderr.write(`✓ Generated SQL for ${rows.length} skills\n`);
